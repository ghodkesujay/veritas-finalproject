    private void AssignBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AssignBtn1ActionPerformed
        int selectedRow = orderTable.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a row from the table to view details","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else{
            ReceiveRequest order  = (ReceiveRequest)orderTable.getValueAt(selectedRow, 0);
            if(order.getStatus().equals("Notified to Blood Bank")){
                JOptionPane.showMessageDialog(null,"Already Notified to Blood Bank.","Warning",JOptionPane.WARNING_MESSAGE);
            }else{
                NotifyBloodBankH viewOrder=new NotifyBloodBankH(userProcessContainer,account,order,system);
                userProcessContainer.add("View Order",viewOrder);
                CardLayout layout=(CardLayout)userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
        }
    }//GEN-LAST:event_AssignBtn1ActionPerformed


