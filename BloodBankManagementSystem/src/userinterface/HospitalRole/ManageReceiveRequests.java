    private void viewOrderBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewOrderBtnActionPerformed
        int selectedRow = orderTable.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a row from the table to change status","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else{
            ReceiveRequest order  = (ReceiveRequest)orderTable.getValueAt(selectedRow, 0);  
            if(order.getStatus().equals("Blood Collected")){
                JOptionPane.showMessageDialog(null,"Blood for this request is already received.","Warning",JOptionPane.WARNING_MESSAGE);
            }else{
                OrderDetails viewOrder=new OrderDetails(userProcessContainer,account,order,system);
                userProcessContainer.add("View Order",viewOrder);
                CardLayout layout=(CardLayout)userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
        }
    }//GEN-LAST:event_viewOrderBtnActionPerformed

