     private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) orderTable.getModel();
        
        model.setRowCount(0);
        
       
        for (Hospital restro:system.getHospitalDirectory().getHospitalList()) {
           
            if (restro.getAdminUName().equals(account.getUsername())) {
                //System.out.println(restro.getOrderList());
               for(ReceiveRequest menu:restro.getOrderList()){
                Object[] row = new Object[2];
               // row[0] = menu;
                row[0] = menu;
                row[1] = menu.getStatus();
                model.addRow(row);
               }
                
            }
            
        }
    }
    
